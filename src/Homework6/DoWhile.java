package Homework6;

public class DoWhile {
        public static void main(String[] args) {
        DoWhile();
    }
    public static int DoWhile(){
        int i = 10;
        do{
            System.out.println(i);
            i--;
        }
        while (i>=1);
        return i;
    }
}


